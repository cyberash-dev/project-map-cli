import type { Root, RootContent } from "mdast";
import { toMarkdown } from "mdast-util-to-markdown";
import { gfmTableToMarkdown } from "mdast-util-gfm-table";
import type { ProjectMap, SectionId } from "../../../core/domain/project-map.js";
import type { ResolvedConfig } from "../../../core/ports/config.port.js";
import { renderContexts } from "../slices/contexts/render.js";
import { renderEndpoints } from "../slices/endpoints/render.js";
import { renderEntities } from "../slices/entities/render.js";
import { renderEnums } from "../slices/enums/render.js";
import { renderInteractions } from "../slices/interactions/render.js";
import { renderStorage } from "../slices/storage/render.js";
import { renderWorkers } from "../slices/workers/render.js";
import { heading, paragraph, table, text } from "./mdast-helpers.js";

export function renderMarkdown(map: ProjectMap, config: ResolvedConfig): string {
  const children: RootContent[] = [];
  children.push(heading(1, `Project Map: ${map.project.name}`));
  children.push(
    paragraph([
      text(
        `Generated by project-map v${map.metadata.toolVersion} on ${map.metadata.generatedAt}` +
          (map.metadata.revision ? ` from revision ${map.metadata.revision}` : ""),
      ),
    ]),
  );
  children.push(
    paragraph([
      text(
        `Coverage: ${map.metadata.scannedFiles} ${map.project.language} files scanned ` +
          `(${map.metadata.excludedFiles} excluded).`,
      ),
    ]),
  );

  for (const id of config.sections) {
    const nodes = renderSection(id, map);
    for (const node of nodes) children.push(node);
  }

  const root: Root = { type: "root", children };
  return toMarkdown(root, {
    bullet: "-",
    emphasis: "_",
    fence: "`",
    fences: true,
    incrementListMarker: true,
    listItemIndent: "one",
    rule: "-",
    strong: "*",
    tightDefinitions: true,
    extensions: [gfmTableToMarkdown()],
  });
}

function renderSection(id: SectionId, map: ProjectMap): RootContent[] {
  switch (id) {
    case "overview":
      return map.metadata.overview
        ? [heading(2, "Overview"), paragraph([text(map.metadata.overview)])]
        : [];
    case "contexts":
      return renderContexts(map.contexts) as RootContent[];
    case "entities":
      return renderEntities(map.entities) as RootContent[];
    case "enums":
      return renderEnums(map.enums) as RootContent[];
    case "endpoints":
      return renderEndpoints(map.endpoints) as RootContent[];
    case "storage":
      return renderStorage(map.storage) as RootContent[];
    case "interactions":
      return renderInteractions(map.interactions) as RootContent[];
    case "workers":
      return renderWorkers(map.workers) as RootContent[];
    case "metadata":
      return renderMetadata(map) as RootContent[];
  }
}

function renderMetadata(map: ProjectMap): RootContent[] {
  return [
    heading(2, "Generation metadata"),
    table(
      ["Metric", "Value"],
      [
        ["Tool version", map.metadata.toolVersion],
        ["Config hash", map.metadata.configHash],
        ["Scanned files", String(map.metadata.scannedFiles)],
        ["Excluded", String(map.metadata.excludedFiles)],
        ["Build duration", `${map.metadata.buildDurationMs} ms`],
        ["Language", map.project.language],
        [
          "Frameworks",
          map.project.frameworks.length > 0 ? map.project.frameworks.join(", ") : "(none)",
        ],
        [
          "Errors",
          map.metadata.errors.length > 0
            ? map.metadata.errors.map((e) => `${e.section}: ${e.message}`).join("; ")
            : "(none)",
        ],
      ],
    ),
  ];
}
